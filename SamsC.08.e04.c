/* Given the following array */
/* write code to initialize all the array elements to 0 */

int stuff[12][10];

int main(void)
{
    int count, x1, x2;

    for (count = 0; count < 12; count++)
    {
        for (count = 0; count < 10; count++)
        {
            stuff[x1][x2] = 0;
        }
    }
}